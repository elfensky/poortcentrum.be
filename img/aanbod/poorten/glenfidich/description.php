<p class="c-about__section__content__paragraph">
Voor het frame gebruiken we steeds monoblok profielen van 70 of 100 mm breed.
Met vulling van 60 – 80 – 100 – 140 mm breedte en tussenruimte naar keuze.
</p>

<p class="c-about__section__content__paragraph">
De profielen kunnen zowel horizontaal als verticaal gemonteerd worden.
Tussen de palen en vleugels word steeds een L profiel bevestigd zodat de
scharnieren niet zichtbaar zijn vanaf de straatkant.
Zoals al onze modellen van <a class="u-link" href="https://poortcentrum.be/poorten">aluminium poorten</a> kan deze zowel als vleugelpoort, schuifpoort en hekwerk
geproduceerd worden.
</p>

<p class="c-about__section__content__paragraph">
Manueel of <a class="u-link" href="https://poortcentrum.be/automatisatie">geautomatiseerd</a>.
Om een perfecte afregeling te kunnen garanderen gebruiken wij bij onze
draaipoorten steeds 3D scharnieren.
</p>