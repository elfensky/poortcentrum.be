<p class="c-about__section__content__paragraph">
De klassieker die perfect past bij verschillende stijlen van woningen.
De Cottage is een klassiek model wat in vele varianten volledig gepersonaliseerd kan
worden naar uw wensen.</p>

<p class="c-about__section__content__paragraph">
Voor het frame gebruiken we steeds monoblok profielen van 100 mm breedte,
Kruisjes bovenaan, en onderaan hebt u keuze uit 60 – 80 – 100 of 140 mm breede
verticale profielen met spatie’s naar keuze in optie kan er achter de verticaal
opstaande profielen een volledig gesloten aluminium plaat gemonteerd worden zodat
u toch de verticale spijlen hebt maar uw <a class="u-link" href="https://poortcentrum.be/poorten">poort</a> onderaan volledig gesloten is.
Gesloten Planchetten van 100 of 200 mm breed behoren tevens tot de mogelijkheid.
</p>

<p class="c-about__section__content__paragraph">
Met en zonder winglets,
Tussen de palen en vleugels word steeds een L profiel bevestigd zodat de
scharnieren niet zichtbaar zijn vanaf de straatzijde.
Zoals al onze modellen kan deze zowel als vleugelpoort, schuifpoort en hekwerk
geproduceerd worden.
Manueel of geautomatiseerd.
