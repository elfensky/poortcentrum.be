<p class="c-about__section__content__paragraph">
    <h3>BASSENTO</h3>
	Strak, vlak, minimalistisch en privacy zijn de troeven van het model Bassento.
	Een volledig gesloten <a class="u-link" href="https://poortcentrum.be/poorten">aluminium poort</a> met een frame van 60 x 40 mm met verschillende aluminium platen van
	3 mm boven elkaar met een tussenruimte van +- 10 mm vooraan op het frame
	gemonteerd zonder zichtbare lasnaden.<br>
	Tussen de palen en vleugels word steeds een L profiel gemonteerd tegen inkijk.
	De platen kunnen tevens tussen het frame gemonteerd worden waardoor u zowel
	aan de binnen als de buitenzijde hetzelfde motief krijgt. Zoals al onze modellen kan
	deze zowel als vleugel en schuifpoort geproduceerd worden.<br>
	Manueel of geautomatiseerd.
	Om een perfecte afregeling te kunnen garanderen gebruiken wij bij onze
	draaipoorten steeds 3D scharnieren.
</p>

<p class="c-about__section__content__paragraph">
	<h3>BASSENTO OPEN</h3>
	Combinatie van vlakke plaat met perfo of open structuur.
	Bijvoorbeeld afwisselend een gesloten plaat gecombineerd met perfoplaat.
	De plaat wordt bij dit model steeds tussen het frame bevestigd, zodat u aan de
	binnen en buiten zijde hetzelfde design krijgt. Tussen de palen en vleugels word
	steeds een L profiel bevestigd tegen inkijk. Zoals al onze modellen kan deze zowel
	als vleugelpoort, schuifpoort of hekwerk geproduceerd worden.
	Manueel of geautomatiseerd.
	Om een perfecte afregeling te kunnen garanderen gebruiken wij bij onze
	draaipoorten steeds 3D scharnieren.    
</p>